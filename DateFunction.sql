SELECT TRUNC(SYSDATE,'MM')+(ROWNUM-1) AS DAYS
FROM DUAL
CONNECT BY LEVEL <= LAST_DAY(SYSDATE) - TRUNC(SYSDATE,'MM') + 1; */

