/* TO_DATE 활용시 12 시간 은 1 에서 12 사이 어야 합니다. 
포맷이 HH24가 아닌 HH인경우 12까지밖에 지원하지 않는다. 
systimestamp의 경우 년월일시분초 밀리세컨드 까지 표현 가능 */

SELECT SYSTIMESTAMP ST
    ,TO_CHAR(SYSTIMESTAMP,'YYYY/MM/DD HH24:MI:SS') CST --- 날짜를 포맷 적용하여 문자열로 변환
    ,EXTRACT(YEAR FROM SYSDATE) Y -- 2022
    ,EXTRACT(MONTH FROM SYSDATE) MM -- 5
    ,EXTRACT(DAY FROM SYSDATE) D -- 19
    ,EXTRACT(HOUR FROM SYSTIMESTAMP) T1 -- 8
    ,EXTRACT(HOUR FROM SYSTIMESTAMP)+9 T2 -- 17 국제 표준시에서 +9
    ,EXTRACT(HOUR FROM SYSTIMESTAMP)+EXTRACT(TIMEZONE_HOUR FROM SYSTIMESTAMP) T3
    ,EXTRACT()
FROM DUAL;

