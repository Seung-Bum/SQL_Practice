--2
SELECT NM
      ,SCORE
FROM QUIZE_SCORE;

--3
SELECT NM
FROM QUIZE_SCORE
GROUP BY NM; 
-- 각 사람들의 점수를 ROW 형태로 묶어준다고 생각하자

--4
SELECT NM
      ,MAX(SCORE)
      ,MIN(SCORE)
FROM QUIZE_SCORE
GROUP BY NM;

--5
SELECT NM
      ,MAX(SCORE) || MIN(SCORE) SCORE_RAG
FROM QUIZE_SCORE
GROUP BY NM;

--6
SELECT NM
      ,MIN(SCORE) || '~' || MAX(SCORE) SCORE_RAG
      ,COUNT(SCORE) 건수
      ,COUNT(DECODE(SCORE, SCORE, 1)) 중복1건처리
FROM QUIZE_SCORE
GROUP BY NM;

